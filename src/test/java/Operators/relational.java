package Operators;

public class relational {
	 public static void main(String[] args) {
	        int a = 26;
	        int b = 19;
	        System.out.println(a < b);  
	        
	        
	        System.out.println(a > b);  
	        
	        
	        System.out.println(a <= b);
	        
	        
	        System.out.println(a >= b);
	        
	        
	        
	        
	        
	    }
}
